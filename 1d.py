def countNeighbours(row, column, board):
    count = 0

    for i in range(-1, 2):
        for j in range(-1, 2):
            if row + i >= 0 and row + i < len(board) and column + j >= 0 and column + j < len(board[0]):
                if not (i == 0 and j == 0):
                    if board[row + i][column + j] == 1:
                        count += 1

    return count


def gameoflife(x_cycle):
    start = [row[:] for row in origin]

    for i in range(x_cycle):
        nextStep = [row[:] for row in start]

        for row in range(len(start)):
            for column in range(len(start[row])):
                if start[row][column] == 1:
                    if countNeighbours(row, column, start) < 2 or countNeighbours(row, column, start) > 3:
                        nextStep[row][column] = 0
                else:
                    if countNeighbours(row, column, start) == 3:
                        nextStep[row][column] = 1

        start = [row[:] for row in nextStep]

    return start
